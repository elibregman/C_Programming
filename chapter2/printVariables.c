#include <stdio.h>

int main(void)
{
    int height = 65; /* height in inches */
    float pie = 3.14;

    /* %d is a placeholder for the int value of height to be printed */
    printf("Height: %d\n", height); 
    
    /* %f by default displays a number with 6 digits after the decimal point  */
    printf("Beginning of pie is: %f\n", pie);
        
    return 0;
}


